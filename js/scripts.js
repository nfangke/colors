$(document).ready(function(){
  $("button#blueB").click(function() {
    $("body").removeClass();
    $("body").addClass("blue-background");
  });

  $("button#greenB").click(function() {
    $("body").removeClass();
    $("body").addClass("green-background");
  });

  $("button#orangeB").click(function() {
    $("body").removeClass();
    $("body").addClass("orange-background");
  });


});
